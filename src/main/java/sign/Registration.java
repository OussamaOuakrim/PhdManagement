    private void SinscrireActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SinscrireActionPerformed
        // Boutton s'inscrire 
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/testdb","root","");
            String sql = "INSERT INTO `info_doc` (`nom`, `prenom`, `email`, `tel`,`Username`, `Password`,`CIN`) VALUES (?,?,?,?,?,?,?)";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1,Nom.getText());
            pst.setString(2,Prenom.getText()); 
            pst.setString(3,Email.getText());
            pst.setString(4,Telephone.getText());
            pst.setString(5,Username.getText());
            pst.setString(6,Password.getText());
            pst.setString(7,CIN.getText());
            int rs = pst.executeUpdate();
            if(rs > 0){
                JOptionPane.showMessageDialog(null,"Inscription reussite ");   
            }
            else{
                JOptionPane.showMessageDialog(null,"Erreur ! Veuillez remplir Ã  nouveau le formuaire  ");   
                Nom.setText("");
                Prenom.setText("");
                Telephone.setText("");
                Email.setText("");
                Username.setText("");
                Password.setText("");
                CIN.setText("");
            }   
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
                               
     
    }//GEN-LAST:event_SinscrireActionPerformed

